const PLATFORM_LABELS = {
  facebook: "Facebook",
  instagram: "Instagram",
  pinterest: "Pinterest",
  youtube: "YouTube",
};

export default function FooterSocialLinks({ socialLinks = [] }) {
  const items = Array.isArray(socialLinks) ? socialLinks.filter((s) => s?.url) : [];
  if (items.length === 0) return null;

  return (
    <ul className="wp-block-social-links has-icon-color has-icon-background-color is-layout-flex footer-social-links">
      {items.map((item) => {
        const platform = String(item.platform || "").toLowerCase();
        const icon = SOCIAL_ICONS[platform];
        if (!icon) return null;
        return (
          <li
            key={platform}
            style={{ color: "#000000", backgroundColor: "#ffffff" }}
            className={`wp-social-link wp-social-link-${platform} wp-block-social-link`}
          >
            <a
              rel="noopener nofollow"
              target="_blank"
              href={item.url}
              className="wp-block-social-link-anchor"
              aria-label={PLATFORM_LABELS[platform] || platform}
            >
              {icon}
            </a>
          </li>
        );
      })}
    </ul>
  );
}

export { PLATFORM_LABELS };
